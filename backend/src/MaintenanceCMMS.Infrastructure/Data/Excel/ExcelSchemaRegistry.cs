    public ExcelFileSchema GetRequired(string schemaName)
    {
        var schema = Schemas.FirstOrDefault(item =>
            string.Equals(item.SchemaName, schemaName, StringComparison.OrdinalIgnoreCase) ||
            string.Equals(item.FileName, schemaName, StringComparison.OrdinalIgnoreCase));

        return schema ?? throw new KeyNotFoundException($"Excel schema '{schemaName}' is not registered.");
    }

    private static ExcelFileSchema Schema(
        string schemaName,
        string fileName,
        string[] naturalKey,
        bool allowsUpdate,
        bool requiresApproval,
        params ExcelColumnSchema[] columns)
    {
        return new ExcelFileSchema(schemaName, fileName, DataWorksheet, columns, naturalKey, allowsUpdate, requiresApproval);
    }

